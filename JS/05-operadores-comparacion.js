/* Operadores relacionales o de comparacion
    Mayor que : a > b
    Menor que : a < b
    Mayor o igual que: a >= b
    Menor o igual que: a <= b
    Igual : a == b
    Distinto : a != b
    Operador estricto : ===
    Operador diferente estricto: !==

*/
let numero1 = 22;
let numero2 = 15;
console.log(numero1 > numero2);
console.log(numero1 < numero2);
console.log(numero1 >= numero2);
console.log(numero1 <= numero2);
console.log(numero1 == numero2);
console.log(numero1 != numero2);
console.log(numero1 === numero2);
console.log(numero1 !== numero2);